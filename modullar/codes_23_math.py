import math

x = 400
print(math.sqrt(x))                 # sqrt - sonning kvadrat ildizini hisoblaydi
print(math.pow(6,2))                # pow (power) - sonning darajasini hisoblaydi    bu yerni ko'rib turganingizdek 6 ning 2-darajasini hisoblaydi ya'ni kvadratini hisoblaydi
print(math.pi)                      # pi - pi ning aniq qiymatini chiqaradi
print(math.log2(8))                 # log2 - sonning logarifmik qiymatini hisoblaydi  
print(math.log10(100))              # log10 - sonning logarifmik qiymatini hisoblaydi